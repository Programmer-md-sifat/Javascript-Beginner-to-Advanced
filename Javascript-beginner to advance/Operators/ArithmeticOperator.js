// Arithmetic Operator Coding ex:



// + Coding ex:

var x = 20;

var y = 30;

var z = x + y;

console.log(z);



// - Coding ex:

var x = 50;

var y = 30;

var z = x - y;

console.log(z);



// * Coding ex:

var x = 50;

var y = 30;

var z = x * y;

console.log(z);



// / Coding ex:

var x = 30;

var y = 5;

var z = x / y;

console.log(z);



// ** Coding ex:

var x = 2;

var y = 4;

var z = 2 ** 4;

console.log(z);



// % Coding ex:

var x = 17;

var y = 4;

var z = x % y;

console.log(z);



// ++ Coding ex:

var x = 17;

var y = 4;

var z = x + y;

z++

console.log(z);



// -- Coding ex:

var x = 17;

var y = 4;

var z = x + y;

z--;

console.log(z);



