// Push Pop Shift Unshift Splice Coding ex:



// Push Coding ex:

var arr = [1, 2, 3, 4, 5, 6, 7];

arr.push(8);

console.log(arr);



// Pop Coding ex:

var arr = [1, 2, 3, 4, 5, 6];

arr.pop();

console.log(arr);



// Shift Coding ex:

var arr = [1, 2, 3, 4, 5, 6, 7];

arr.shift();

console.log(arr);



// Unshift Coding ex:

var arr = [1, 2, 3, 4, 5, 6, 7, 8];

arr.unshift(0);

console.log(arr);



// Splice Coding ex:


var arr = [1, 2, 3, 4, 5, 6];

arr.splice(2, 1);

console.log(arr);


// Another Splice:

var arr = [1, 2, 3, 4, 5, 6];

arr.splice(2, 2);

console.log(arr);



