// Function & Without Function Coding Ex:


// Function:

function addNumbers(num1, num2) {

    return num1 + num2;
  }
  
const result = addNumbers(3, 7);

console.log("The sum is:", result);

  
// Without Function:

const num1 = 3;
const num2 = 7;

const sum = num1 + num2;

console.log("The sum is:", sum);


