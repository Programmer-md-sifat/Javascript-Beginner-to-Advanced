// Switch Coding ex:


var age = 18;


switch(age) {
    case 16:
        console.log("You can drive.");
        break;

    case 18:
        console.log("You can vote.");
        break;

    case 21:
        console.log("You can drink Mojo.");
        break;
    
    default:
        console.log("Sorry, you're too young.");
        break;
}


// Output: You can vote.


