// Else If Coding ex:


var score = 75;

if(score >= 1 && score < 50) {
    console.log("Fail");
}

else if(score >= 50 && score < 60) {
    console.log("B");
}

else if(score >= 60 && score < 70) {
    console.log("A");
}

else if(score >= 70 && score <= 100) {
    console.log("A+");
}

else {
    console.log("You are rejected from the College");
}

// Output: You got a A+


