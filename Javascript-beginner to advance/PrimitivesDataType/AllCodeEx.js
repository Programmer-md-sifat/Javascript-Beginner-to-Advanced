// Primitives Data Type Coding Ex:
7


// Number

let myNumber = 42;
console.log(typeof myNumber);

// Output: "number"



// String

let myString = "Hello, world!";
console.log(typeof myString);

// Output: "string"



// Boolean

let isTrue = true;
console.log(typeof isTrue);

// Output: "boolean"



// Undefined

let undefinedVar;
console.log(typeof undefinedVar);

// Output: "undefined"



// Null

let nullVar = null;
console.log(typeof nullVar);

// Output: "object" (Note: typeof null returns "object", which is a historical quirk in JavaScript)



// Symbol

let sym = Symbol('description');
console.log(typeof sym);

// Output: "symbol"


